    <h2>Biography</h2>

    <p>The artistic journey to this point has been far from an easy one. Having completed my degree in fine art in 1992
       from Brighton University, despite pockets of activity, progress and development has been seriously affected and
       curtailed by an undiagnosed mental health condition. Dysthymia is a mood disorder consisting of the same
       cognitive and physical problems as in depression, with less severe but longer-lasting symptoms. Suffering from
       this un-diagnosed condition my whole adult life offered a serious impairment to my ability to commit to and
       develop my style and a portfolio.
       .</p></br>

    <p>Since diagnoses and effective treatment however in 2010, my health and in turn the work has flourished. Now
       happily married and ensconced in my own studio in the heart of the South Downs, I have found the perfect place
       both physically and emotionally to fully explore the nature of the work and my surroundings.</p>
</article>

