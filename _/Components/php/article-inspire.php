    <h2 class="inspireTitle">The Inspiration</h2>

    <p>
        My paintings explore the relationship between us, the landscape and the ephemeral fragile nature of our
        existence. My aim is to create the feeling of space with references to not only what is physically present
        and accessible to the five senses, but what emotions, feelings and inspiration our connections with the space
        inspires in us. My work and the visual language I have created is based on the sights and sounds of what
        surrounds us in nature.
    </p></br>

    <p> Living in the heart of the National Park on the South Downs in East Sussex in the small village of South
        Heighton allows this process to flourish and develop on a daily basis, continuously informing my work as the
        seasons and elements conspire to paint their own eternal stories across the landscape, sky and sea.
    </p></br>

    <p>My cottage, situated right on top of the Downs provides views down to the sea as well as stunning panoramic views
       of The South Downs National Park and The Ouse Valley.
    </p></br>

    <p>Daily trips onto the downs have built an intimate knowledge of, and feeling for the landscape and its flora and
       fauna. Sketches, memories and photos are then taken back to the studio to encompass into to my work.
    </p>
